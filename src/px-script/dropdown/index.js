const _generateDropdown = dropdown => {
    const selectItems = [];

    dropdown.querySelectorAll("a").forEach(item => {
        selectItems.push({
            _element: item,
            label: item.innerText,
            href: item.attributes.href.value,
            icon: item.attributes["data-dropdown-icon"].value
        });
    });

    console.log(selectItems);
};

export default {
    init: () => {
        const dropdowns = document.querySelectorAll(".dropdown[data-dropdown]");

        dropdowns.forEach(dropdown => {
            _generateDropdown(dropdown);
        });
    }
};
